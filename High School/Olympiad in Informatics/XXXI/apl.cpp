#include <bits/stdc++.h>

using namespace std;

typedef unsigned long long ull;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        ull a, b;
        cin >> a >> b;
        cout << a + b << '\n';
    }
}